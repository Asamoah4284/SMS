function styleHeader(row) {
  row.font = { bold: true, color: { argb: COLOR_HEADER_FG }, size: 11 };
  row.fill = { type: 'pattern', pattern: 'solid', fgColor: { argb: COLOR_HEADER } };
  row.alignment = { vertical: 'middle', horizontal: 'left', wrapText: true };
  row.height = 28;
}

function styleExampleRow(row) {
  row.fill = { type: 'pattern', pattern: 'solid', fgColor: { argb: COLOR_EXAMPLE } };
  row.font = { italic: true, color: { argb: 'FF375623' } };
}

function markAutoColumn(sheet, colLetter, lastRow = 50) {
  for (let r = 2; r <= lastRow; r++) {
    const cell = sheet.getCell(`${colLetter}${r}`);
    cell.fill = { type: 'pattern', pattern: 'solid', fgColor: { argb: COLOR_AUTO } };
  }
}

function addDropdown(sheet, colLetter, values, firstRow = 2, lastRow = 500) {
  const formula = `"${values.join(',')}"`;
  for (let r = firstRow; r <= lastRow; r++) {
    sheet.getCell(`${colLetter}${r}`).dataValidation = {
      type: 'list',
      allowBlank: true,
      formulae: [formula],
      showErrorMessage: true,
      errorStyle: 'warning',
      errorTitle: 'Invalid value',
      error: 'Pick from the dropdown list.',
    };
  }
}

function setColumns(sheet, defs) {
  sheet.columns = defs.map((d) => ({ header: d.header, key: d.key, width: d.width || 20 }));
  styleHeader(sheet.getRow(1));
  sheet.views = [{ state: 'frozen', ySplit: 1 }];
}

async function build() {
  const wb = new ExcelJS.Workbook();
  wb.creator = 'EduTrack SMS';
  wb.created = new Date();
  wb.title = 'School Onboarding Template';

  buildReadme(wb);
  buildSchoolInfo(wb);
  buildClasses(wb);
  buildSubjects(wb);
  buildStaff(wb);
  buildStudents(wb);
  buildFees(wb);
  buildTerms(wb);

  fs.mkdirSync(path.dirname(OUTPUT_PATH), { recursive: true });
  await wb.xlsx.writeFile(OUTPUT_PATH);
  console.log(`Wrote ${OUTPUT_PATH}`);
}

